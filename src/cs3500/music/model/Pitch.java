package cs3500.music.model;

/**
 * Represents a pitch in the form of the provider's model.
 */
public class Pitch {
  /**
   * Replaces the provider's value function to return the value function for our version of pitch.
   * @return an array of pitches from our version of pitch
   */
  public static Pitches[] values() {
    return Pitches.values();
  }
}